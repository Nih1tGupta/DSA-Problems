class Solution {
  public:
    int minRepeats(string& s1, string& s2) {
        // code here
          int count = 1;
        string temp =s1;
        for(int i=0; i<800; i++){
            if(temp.find(s2) != string::npos){
                return count;
            }
            temp += s1;
            count ++;
        }
        return -1;
    }
};

