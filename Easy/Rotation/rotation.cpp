class Solution{
public:	
	int findKRotation(int arr[], int n) {
    int first=0;int last=n-1;
    int result =0;
    while(first <= last ){
        if(arr[first]>arr[last]){
            result +=1;
        }
        first +=1;
    }
    return result;
}
}; 

