class Solution {
  public:
    int swapNibbles(int n) {
        int f=n%16;
        int l=n/16;
        return f*16+l;
    }
};

//{ Driver Code Starts.
int main() {
    int t;
    cin >> t;
    while (t--) {
        int n;
        cin >> n;
        Solution ob;
        cout << ob.swapNibbles(n) << endl;
    }
    return 0;
}
